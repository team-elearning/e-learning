<script setup lang="ts">
import { ref, reactive } from 'vue'
import { authService } from '@/services/auth.service'
import { useRouter } from 'vue-router'

const router = useRouter()

const loading = ref(false)
const showPassword = ref(false)
const showConfirm = ref(false)

const form = reactive({
  fullName: '',
  username: '',
  email: '',
  phone: '',
  password: '',
  confirm: '',
  agree: false,
})

const errors = reactive({
  fullName: '',
  username: '',
  email: '',
  phone: '',
  password: '',
  confirm: '',
  agree: '',
})

function validateFullName() {
  if (!form.fullName) {
    errors.fullName = 'Vui lòng nhập họ tên'
    return false
  }
  errors.fullName = ''
  return true
}

function validateUsername() {
  if (!form.username) {
    errors.username = 'Vui lòng nhập username'
    return false
  }
  errors.username = ''
  return true
}

function validateEmail() {
  if (!form.email) {
    errors.email = 'Vui lòng nhập email'
    return false
  }
  if (!/^[^\s@]+@[^\s@]+\.[^\s@]+$/.test(form.email)) {
    errors.email = 'Email không hợp lệ'
    return false
  }
  errors.email = ''
  return true
}

function validatePhone() {
  if (!form.phone) {
    errors.phone = 'Vui lòng nhập số điện thoại'
    return false
  }
  if (!/^0\d{9,10}$/.test(form.phone)) {
    errors.phone = 'Số điện thoại không hợp lệ'
    return false
  }
  errors.phone = ''
  return true
}

function validatePassword() {
  if (!form.password) {
    errors.password = 'Vui lòng nhập mật khẩu'
    return false
  }
  if (form.password.length < 6) {
    errors.password = 'Mật khẩu tối thiểu 6 ký tự'
    return false
  }
  errors.password = ''
  return true
}

function validateConfirm() {
  if (!form.confirm) {
    errors.confirm = 'Vui lòng nhập lại mật khẩu'
    return false
  }
  if (form.password !== form.confirm) {
    errors.confirm = 'Mật khẩu không khớp'
    return false
  }
  errors.confirm = ''
  return true
}

function validateAgree() {
  if (!form.agree) {
    errors.agree = 'Vui lòng đồng ý với điều khoản'
    return false
  }
  errors.agree = ''
  return true
}

function validate() {
  const fullNameValid = validateFullName()
  const usernameValid = validateUsername()
  const emailValid = validateEmail()
  const phoneValid = validatePhone()
  const passwordValid = validatePassword()
  const confirmValid = validateConfirm()
  const agreeValid = validateAgree()
  return fullNameValid && usernameValid && emailValid && phoneValid && passwordValid && confirmValid && agreeValid
}

const onSubmit = async () => {
  if (!validate()) return

  loading.value = true
  try {
    await authService.register({
      fullName: form.fullName,
      username: form.username,
      email: form.email,
      phone: form.phone,
      password: form.password,
      confirm: form.confirm,
      agree: form.agree,
    })
    showToast('Đăng ký thành công. Vui lòng đăng nhập.', 'success')
    router.push('/auth/login')
  } catch (e: any) {
    showToast(e?.message || 'Đăng ký thất bại', 'error')
  } finally {
    loading.value = false
  }
}

function showToast(message: string, type: 'success' | 'error') {
  const toast = document.createElement('div')
  toast.className = `toast-notification ${type === 'success' ? 'toast-success' : 'toast-error'}`
  toast.innerHTML = `
    <div class="toast-icon-wrap">
      ${type === 'success' 
        ? '<svg class="toast-icon" fill="none" stroke="currentColor" viewBox="0 0 24 24"><path stroke-linecap="round" stroke-linejoin="round" stroke-width="2.5" d="M9 12l2 2 4-4m6 2a9 9 0 11-18 0 9 9 0 0118 0z"/></svg>'
        : '<svg class="toast-icon" fill="none" stroke="currentColor" viewBox="0 0 24 24"><path stroke-linecap="round" stroke-linejoin="round" stroke-width="2.5" d="M10 14l2-2m0 0l2-2m-2 2l-2-2m2 2l2 2m7-2a9 9 0 11-18 0 9 9 0 0118 0z"/></svg>'
      }
    </div>
    <div class="toast-body">
      <div class="toast-title">${type === 'success' ? 'Thành công!' : 'Lỗi!'}</div>
      <div class="toast-msg">${message}</div>
    </div>
    <div class="toast-bar"></div>
  `
  document.body.appendChild(toast)
  requestAnimationFrame(() => {
    toast.style.transform = 'translateX(0)'
    toast.style.opacity = '1'
  })
  setTimeout(() => {
    toast.style.transform = 'translateX(400px)'
    toast.style.opacity = '0'
    setTimeout(() => toast.remove(), 300)
  }, 3500)
}
</script>

